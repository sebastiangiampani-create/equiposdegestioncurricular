let currentIndex = 0;

const chapterLabel = document.getElementById("chapterLabel");
const episodeTitle = document.getElementById("episodeTitle");
const audioPlayer = document.getElementById("audioPlayer");
const episodeSummary = document.getElementById("episodeSummary");
const ideasGrid = document.getElementById("ideasGrid");
const chapterButtonsContainer = document.getElementById("chapterButtons");
const prevBtn = document.getElementById("prevBtn");
const nextBtn = document.getElementById("nextBtn");

function buildChapterButtons() {
  chapters.forEach((chapter, index) => {
    const button = document.createElement("button");
    button.className = index === 0 ? "chapter-btn active" : "chapter-btn";
    button.type = "button";
    button.dataset.index = String(index);
    button.innerHTML = `<span>${chapter.label}</span>${chapter.title}`;
    button.addEventListener("click", () => renderChapter(index, true));
    chapterButtonsContainer.appendChild(button);
  });
}

function renderChapter(index, autoplay = false) {
  const chapter = chapters[index];
  currentIndex = index;

  chapterLabel.textContent = chapter.label;
  episodeTitle.textContent = chapter.title;
  audioPlayer.src = chapter.audio;
  episodeSummary.textContent = chapter.summary;

  ideasGrid.innerHTML = "";
  chapter.ideas.forEach((idea) => {
    const card = document.createElement("div");
    card.className = "idea-card";
    card.textContent = idea;
    ideasGrid.appendChild(card);
  });

  document.querySelectorAll(".chapter-btn").forEach((button, buttonIndex) => {
    button.classList.toggle("active", buttonIndex === index);
  });

  if (autoplay) {
    const playPromise = audioPlayer.play();
    if (playPromise !== undefined) {
      playPromise.catch(() => {
        // Algunos navegadores bloquean autoplay. El usuario puede tocar play manualmente.
      });
    }
  }
}

prevBtn.addEventListener("click", () => {
  const newIndex = currentIndex === 0 ? chapters.length - 1 : currentIndex - 1;
  renderChapter(newIndex, true);
});

nextBtn.addEventListener("click", () => {
  const newIndex = currentIndex === chapters.length - 1 ? 0 : currentIndex + 1;
  renderChapter(newIndex, true);
});

audioPlayer.addEventListener("ended", () => {
  const newIndex = currentIndex === chapters.length - 1 ? 0 : currentIndex + 1;
  renderChapter(newIndex, false);
});

buildChapterButtons();
renderChapter(0, false);
